・takeメソッドは、
配列の先頭から引数n個の要素を取り出して、
新しい配列を作成して返します。

animals = ["mouse", "cow", "tiger", "rabbit", "dragon", "snake"]
p animals.take(3)
↓
["mouse", "cow", "tiger"]
----------------------------------------------------
・任意の数を先頭から取得
a = [1,2,3,4,5]
puts a.take(3)
↓
1
2
3
----------------------------------------------------
